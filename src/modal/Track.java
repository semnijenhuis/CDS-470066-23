package modal;

public class Track {

    private String fromStation;
    private String toStation;
    private int trackNumber1;
    private int trackNumber2;
    private int trackNumber3;

    public Track(String fromStation, String toStation, int trackNumber1, int trackNumber2, int trackNumber3) {
        this.fromStation = fromStation;
        this.toStation = toStation;
        this.trackNumber1 = trackNumber1;
        this.trackNumber2 = trackNumber2;
        this.trackNumber3 = trackNumber3;
    }


}
